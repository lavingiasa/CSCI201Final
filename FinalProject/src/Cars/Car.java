package Cars;

public class Car 
{
	private int id;
	private double speed;
	private String direction;
	private String ramp;
	private String freeway;
	
	public Car(int id, double speed, String direction, String ramp, String freeway)
	{
		this.id = id;
		this.speed = speed;
		this.direction = direction;
		this.ramp = ramp;
		this.freeway = freeway;
	}
	
}
